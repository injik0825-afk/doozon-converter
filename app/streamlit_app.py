def process_kp_securities(df, market):
    """
    케이피 단기매매증권 거래내역 파서 (KOSPI/KOSDAQ 시트별 호출).
    
    주의: 시트에는 9개월치 거래가 모두 들어있음. 3월 거래만 필터링!
    
    헤더: [종목코드, 종목명, 매매내역, 매매일자, 매매수량, 매매금액, 원가,
           매매수수료, 매매손익, 잔고, 기준가, 평가, 평가손익]
    
    매매내역 종류:
    - 평가: 월말평가 (자동변환 영역 외, 무시)
    - 매수: 차변 단매증(KS/KQ)/증권거래수수료 / 대변 미수금
    - 매도: 차변 미수금/증권거래수수료/세공 / 대변 단매증(KS/KQ)/처분이익
    - 공모주입고: 차변 단매증 / 대변 선급금
    - 공모주출고: 무시 (자산 이동)
    """
    rows, unmapped = [], []
    if df.empty: return rows, unmapped

    # 시장 코드 → 계정 키 매핑
    if 'KOSPI' in market or 'KS' in market:
        sm_acct, gain_acct, loss_acct = '단매증KS', '처분이익KS', '처분손실KS'
        market_tag = 'KS'
    else:  # KOSDAQ
        sm_acct, gain_acct, loss_acct = '단매증KQ', '처분이익KQ', '처분손실KQ'
        market_tag = 'KQ'

    # 헤더는 행 0
    for i in range(1, len(df)):
        r = df.iloc[i]
        ttype = clean(r.iloc[2])
        date_val = clean(r.iloc[3])
        if not ttype or not date_val: continue
        if ttype == '평가': continue  # 월말평가 무시

        m, d = parse_date(str(date_val))
        if not m: continue
        # 3월 거래만 처리 (시트에 다른 월 거래도 포함됨)
        if m != 3: continue
        # 2026년 거래만 (날짜 문자열에 '2026' 포함 확인)
        if '2026' not in str(date_val): continue

        stock = clean(r.iloc[1])
        qty = to_int(r.iloc[4])
        amount = to_int(r.iloc[5])  # 매매금액
        cost = to_int(r.iloc[6])    # 원가
        comm = to_int(r.iloc[7])    # 수수료
        gain = to_int(r.iloc[8])    # 매매손익

        sl = get_stock(stock) if stock else ''

        if ttype == '매수':
            # 차변 단매증/수수료, 대변 기타제예금
            cost_amt = abs(cost) if cost else amount
            memo = f'{sl}({qty}주)매수' if sl else f'{stock} 매수'
            rows.append(row(m, d, '차변', sm_acct, sl, memo, cost_amt, 0))
            if comm > 0:
                rows.append(row(m, d, '차변', '증권거래수수료', sl, '매수수수료', comm, 0))
            rows.append(row(m, d, '대변', '기타제예금', '미래에셋대우', memo, 0, cost_amt + comm))
            continue

        if ttype == '매도':
            qty_abs = abs(qty)
            amount_abs = abs(amount)  # 매도금액(시트)
            cost_abs = abs(cost) if cost else 0
            gain_abs = gain  # 매매손익 (양수=이익, 음수=손실)

            # 거래세 추산: 매매금액 - 수수료 - (원가 + 손익) = 거래세
            # 분개장 패턴: 기타제예금 = 매매금액 - 수수료 - 거래세
            # 거래세 = 매매금액 - 수수료 - 원가 - 손익 (정산 후 잔액 차이)
            tax = 0
            net_in = amount_abs - comm  # 거래세 0 가정 (시트에 표기 없음)
            # 매도손익이 매매금액-원가와 차이가 나면 그 차이 = 수수료+거래세
            # 일단 단순화: 거래세는 자동변환에서 표기 안 함 (분개장 정답에 별도 분개로 들어감)

            memo = f'{sl}({qty_abs}주)매도' if sl else f'{stock} 매도'
            rows.append(row(m, d, '차변', '기타제예금', '미래에셋대우', memo, net_in, 0))
            if comm > 0:
                rows.append(row(m, d, '차변', '증권거래수수료', sl, '매도수수료', comm, 0))
            if cost_abs > 0:
                rows.append(row(m, d, '대변', sm_acct, sl, memo, 0, cost_abs))
            if gain_abs > 0:
                rows.append(row(m, d, '대변', gain_acct, sl, memo + '처분이익', 0, gain_abs))
            elif gain_abs < 0:
                rows.append(row(m, d, '차변', loss_acct, sl, memo + '처분손실', abs(gain_abs), 0))
            continue

        if '공모주입고' in ttype or '입고' in ttype:
            # 차변 단매증 / 대변 선급금
            cost_amt = abs(cost) if cost else amount
            memo = f'{sl}({qty}주)공모주입고' if sl else f'{stock} 공모주입고'
            rows.append(row(m, d, '차변', sm_acct, sl, memo, cost_amt, 0))
            rows.append(row(m, d, '대변', '선급금', sl, memo, 0, cost_amt))
            continue

        if '출고' in ttype:
            continue  # 자산 이동, 분개 없음

        # 미인식
        unmapped.append({'날짜': f'{m}/{d}', '시장': market_tag, '매매내역': ttype,
                         '종목': stock, '수량': qty, '금액': amount})

    return rows, unmapped


def process_kp_bank(df, account_id):
    """
    케이피 신한/하나은행 거래명세 파서.
    헤더: [거래일시, 적요, 입금액, 출금액, 내용, 잔액, 거래점명, 입금인코드, 메모]
    
    행 수가 매우 많아(신한160 = 3588행) 3월 거래만 필터링.
    """
    rows, unmapped = [], []
    if df.empty: return rows, unmapped

    # 헤더 찾기 (보통 행 0)
    hdr = None
    for i in range(min(5, len(df))):
        vals = [clean(v) for v in df.iloc[i].values]
        if '거래일시' in vals and '입금액' in vals and '출금액' in vals:
            hdr = i; break
    if hdr is None: return rows, unmapped

    df.columns = df.iloc[hdr]
    df = df.iloc[hdr+1:].reset_index(drop=True)

    for _, r in df.iterrows():
        date_val = clean(r.get('거래일시', ''))
        if not date_val: continue
        # 3월 거래만 (날짜 문자열에 '2026.03' 또는 '2026-03' 포함)
        if '2026.03' not in date_val and '2026-03' not in date_val and '26.03' not in date_val:
            continue
        m, d = parse_date(str(date_val))
        if not m or m != 3: continue

        in_amt = to_int(r.get('입금액', 0))
        out_amt = to_int(r.get('출금액', 0))
        if in_amt == 0 and out_amt == 0: continue

        memo = clean(r.get('적요', ''))
        content = clean(r.get('내용', ''))
        combined = memo + ' ' + content

        result = classify_kp_bank(m, d, in_amt, out_amt, memo, content, combined, account_id)
        if result:
            rows.extend(result)
        elif result == []:
            pass
        else:
            unmapped.append({'날짜': f'{m}/{d}', '입금': in_amt, '출금': out_amt,
                             '적요': memo, '내용': content, '계좌': account_id})

    return rows, unmapped


def classify_kp_bank(month, day, in_amt, out_amt, memo, content, combined, account_id):
    """케이피 은행 거래 분류 (분개장 정답 기반 패턴)"""
    rows = []
    cp_self = f'{account_id}'

    # 자기 계좌간 이체 무시
    if '계좌이체' in memo or '자기이체' in memo:
        return []

    # 한국연금투자자(자기 회사명) 출금 = 자기 계좌간 자금이체 또는 펀드환급 짝 분개
    # 분개장에는 짝 분개로만 처리되므로 무시
    if '한국연금투자자' in content and out_amt > 0:
        return []
    # 케이피자산운용(변경 후 회사명) 동일 처리
    if '케이피자산운용' in content and out_amt > 0:
        return []

    # 신한투자증권/교보증권/미래에셋 등 증권사 출금 = 자금이체 (단매증 거래내역에서 처리됨)
    if any(k in content for k in ['신한투자증권','교보증권','미래에셋','한국투자증권',
                                    'NH투자증권','KB증권','삼성증권','키움증권']) and out_amt > 0:
        return []

    # ── 출금 ──
    if out_amt > 0:
        # 카드결제
        if '하나카드' in memo or '카드결제' in memo:
            rows += [row(month, day, '차변', '미지급금', '하나카드', memo, out_amt, 0),
                     row(month, day, '대변', '보통예금', cp_self,    memo, 0, out_amt)]
            return rows

        # 4대보험
        if '국민연금' in combined:
            half = out_amt // 2
            rows += [row(month, day, '차변', '예수금',     '국민연금공단', memo, half, 0),
                     row(month, day, '차변', '세금과공과금','국민연금공단', memo, out_amt - half, 0),
                     row(month, day, '대변', '보통예금',   cp_self,        memo, 0, out_amt)]
            return rows
        if '국민건강' in combined or '건강보험' in combined:
            half = out_amt // 2
            rows += [row(month, day, '차변', '예수금',     '건강보험공단', memo, half, 0),
                     row(month, day, '차변', '복리후생비', '건강보험공단', memo, out_amt - half, 0),
                     row(month, day, '대변', '보통예금',   cp_self,        memo, 0, out_amt)]
            return rows
        if '고용보험' in combined:
            emp = round(out_amt * 18900 / 42800)
            rows += [row(month, day, '차변', '예수금',   '근로복지공단', memo, emp, 0),
                     row(month, day, '차변', '보험료',   '근로복지공단', memo, out_amt - emp, 0),
                     row(month, day, '대변', '보통예금', cp_self,        memo, 0, out_amt)]
            return rows
        if '산재보험' in combined:
            rows += [row(month, day, '차변', '보험료',   '근로복지공단', memo, out_amt, 0),
                     row(month, day, '대변', '보통예금', cp_self,        memo, 0, out_amt)]
            return rows

        # 세금
        if '국세' in combined or '원천세' in combined:
            rows += [row(month, day, '차변', '예수금',   '세무서', memo, out_amt, 0),
                     row(month, day, '대변', '보통예금', cp_self,  memo, 0, out_amt)]
            return rows
        if '지방세' in combined or '지방소득세' in combined:
            rows += [row(month, day, '차변', '예수금',   '구청', memo, out_amt, 0),
                     row(month, day, '대변', '보통예금', cp_self, memo, 0, out_amt)]
            return rows

        # FB보험 → 보험료
        if memo == 'FB보험' or '보험료' in memo:
            rows += [row(month, day, '차변', '보험료',   content, memo, out_amt, 0),
                     row(month, day, '대변', '보통예금', cp_self, memo, 0, out_amt)]
            return rows

        # FB자동/케이카렌트 → 차량유지비 (리스/렌트)
        # 단 분개장은 미지급금/보통예금으로 처리. 케이카렌트도 그렇게 처리.
        # → 일반 BZ뱅크 패턴과 동일하게 미지급금 차감으로 통합

        # BZ급여 → 미지급금(급여) / 보통예금
        if memo == 'BZ급여' or '급여' in memo:
            rows += [row(month, day, '차변', '미지급금', '급여', memo, out_amt, 0),
                     row(month, day, '대변', '보통예금', cp_self, memo, 0, out_amt)]
            return rows

        # 한국연금투자자 출금 + 작은 금액 (~2,000원) → 이체수수료/지급수수료
        if ('한국연금' in content or '한국연금' in memo) and out_amt < 10000:
            rows += [row(month, day, '차변', '지급수수료', '거래처미지정', memo, out_amt, 0),
                     row(month, day, '대변', '보통예금',   cp_self,       memo, 0, out_amt)]
            return rows

        # BZ뱅크 / 타행환 / FB자동 / FB이체 / 타행MB / 타행PC / 금결PG / 지로 / 타행
        # 분개장 정답 패턴: 차변 미지급금 / 대변 보통예금 (대부분 사전 계상된 비용 결제)
        if any(k in memo for k in ['BZ뱅크','타행환','FB이체','FB자동','타행MB','타행PC',
                                     '금결PG','지로','타행']):
            # 거래처는 content (상대방 회사/사람 이름)
            partner = content if content else memo
            rows += [row(month, day, '차변', '미지급금', partner, content or memo, out_amt, 0),
                     row(month, day, '대변', '보통예금', cp_self, content or memo, 0, out_amt)]
            return rows

        # 통신비
        if any(k in combined for k in ['SMS','SKT','KT','케이티','LG U','LGU','인터넷','SKB']):
            rows += [row(month, day, '차변', '통신비',   '', memo, out_amt, 0),
                     row(month, day, '대변', '보통예금', cp_self, memo, 0, out_amt)]
            return rows

        # 임차료
        if '임차' in combined or '임대' in combined:
            rows += [row(month, day, '차변', '지급임차료', '', memo, out_amt, 0),
                     row(month, day, '대변', '보통예금',   cp_self, memo, 0, out_amt)]
            return rows

        # 이체수수료
        if '이체수수료' in combined or '수수료' in memo:
            rows += [row(month, day, '차변', '지급수수료', '', memo, out_amt, 0),
                     row(month, day, '대변', '보통예금',   cp_self, memo, 0, out_amt)]
            return rows

    # ── 입금 ──
    if in_amt > 0:
        # 강민석 등 직원 대여금 이자 (소액 입금) - 적요에 인명만
        # 분개장: 차변 보통예금 / 대변 대여금수취이자
        # 다만 일반화 어려움. 패턴: 작은 금액 + 인명 = 대여금 이자
        # 판단 기준: 적요 = 'BZ뱅크/타행MB/타행PC' + 내용에 인명 + 입금
        # 보수적으로: 회사명/주식회사 등 미포함 + 짧은 한글 이름

        # 자산관리/투자자문/투자일임/플랫폼 수수료 입금
        if '자산관리' in combined:
            rows += [row(month, day, '차변', '보통예금',     cp_self, memo, in_amt, 0),
                     row(month, day, '대변', '자산관리수수료','',     memo, 0, in_amt)]
            return rows
        if '투자자문' in combined or '자문' in combined:
            rows += [row(month, day, '차변', '보통예금',     cp_self, memo, in_amt, 0),
                     row(month, day, '대변', '투자자문수수료','',     memo, 0, in_amt)]
            return rows
        if '투자일임' in combined or '일임' in combined:
            rows += [row(month, day, '차변', '보통예금',     cp_self, memo, in_amt, 0),
                     row(month, day, '대변', '투자일임수수료','',     memo, 0, in_amt)]
            return rows
        if '플랫폼' in combined:
            rows += [row(month, day, '차변', '보통예금',     cp_self, memo, in_amt, 0),
                     row(month, day, '대변', '플랫폼수수료', '',     memo, 0, in_amt)]
            return rows

        # 모아저축은행 입금 → 고객예수금 (분개장 정답 패턴)
        if '모아저축' in content or '모아저축' in combined:
            rows += [row(month, day, '차변', '보통예금',   cp_self, memo, in_amt, 0),
                     row(month, day, '대변', '고객예수금', content or '주식회사 모아저축은행', memo, 0, in_amt)]
            return rows

        # 디케이메디칼솔루션 등 자문 수수료 입금 (외상매출금 회수)
        # 적요 = '타행PC' / 내용 = 디케이메디칼솔...
        if '디케이메디칼' in content or '디케이메디칼' in combined:
            rows += [row(month, day, '차변', '보통예금',   cp_self, memo, in_amt, 0),
                     row(month, day, '대변', '외상매출금', content, memo, 0, in_amt)]
            return rows

        # 강민석 등 인명 입금 (소액) → 대여금수취이자
        # 정확한 매칭 어려움. 일단 미분류로 두기보다 외상매출금 회수로 추정
        # (분개장 정답 인명 직접 입금은 대여금수취이자뿐인 듯하지만 적은 금액에만 적용)
        # → 일반 인명 입금은 외상매출금 회수로 처리
        if memo in ('타행환','타행MB','타행PC','BZ뱅크') and content and len(content) <= 5:
            # 짧은 한글 이름은 직원/개인 이체. 대여금수취이자 가능성
            if in_amt < 500000 and content.replace(' ','').isalpha() == False:
                # 안전하게 외상매출금
                rows += [row(month, day, '차변', '보통예금',   cp_self, memo, in_amt, 0),
                         row(month, day, '대변', '외상매출금', content, memo, 0, in_amt)]
                return rows

        # 이자입금
        if '이자' in combined or '결산' in combined:
            rows += [row(month, day, '차변', '보통예금', cp_self, memo, in_amt, 0),
                     row(month, day, '대변', '예적금이자','',     memo, 0, in_amt)]
            return rows

        # 일반 BZ/타행 입금 → 외상매출금 회수
        if any(k in memo for k in ['BZ뱅크','타행환','타행MB','타행PC','FB이체']):
            partner = content if content else memo
            rows += [row(month, day, '차변', '보통예금',   cp_self, memo, in_amt, 0),
                     row(month, day, '대변', '외상매출금', partner, memo, 0, in_amt)]
            return rows

    return None


def process_kp_card(df, sheet_name):
    """
    케이피 하나카드 승인내역 파서 (시트별 = 카드별).
    시트명에서 카드번호 끝 4자리 추출 (하나3034 → 3034).
    
    헤더 위치: 행 5 (요약정보 행 0~4 다음).
    헤더: [NO, 이용일, 이용시간, 카드번호, 승인번호, 승인금액, 승인취소금액,
           가맹점명, 업종명, 가맹점번호, 가맹점사업자번호, 이용구분, 할부기간, 매입, 매입금액, ...]
    
    분개:
    - 차변 비용계정 (또는 부가세대급금 + 비용)
    - 대변 미지급금 (거래처 = 하나카드/{4자리})
    """
    rows, unmapped = [], []
    if df.empty: return rows, unmapped

    # 카드번호 끝 4자리
    m_card = re.search(r'(\d{4})$', sheet_name)
    card_short = m_card.group(1) if m_card else 'XXXX'
    cp = f'하나카드/{card_short}'

    # 헤더 찾기
    hdr = None
    for i in range(min(15, len(df))):
        vals = [clean(v) for v in df.iloc[i].values]
        if 'NO' in vals and '이용일' in vals and '승인금액' in vals:
            hdr = i; break
    if hdr is None: return rows, unmapped

    df.columns = df.iloc[hdr]
    df = df.iloc[hdr+1:].reset_index(drop=True)

    for _, r in df.iterrows():
        date_val = clean(r.get('이용일', ''))
        if not date_val: continue
        m, d = parse_date(str(date_val))
        if not m: continue

        amt = to_int(r.get('승인금액', 0))
        취소 = to_int(r.get('승인취소금액', 0))
        if amt <= 0 or 취소 > 0: continue

        store = clean(r.get('가맹점명', ''))
        biz = clean(r.get('업종명', ''))

        # 비용계정 분류 (분개장 분석 결과 기반)
        if any(k in biz for k in ['일반음식점','일식','중식','서양식','패스트푸드',
                                    '제과','아이스크림','기타식음료품','호프','기타음식점']):
            acct = '복리후생비'
        elif '일반주점' in biz or '주점' in biz:
            acct = '접대비'
        elif any(k in biz for k in ['택시','고속도로통행','주차','주유','자동차',
                                      '지하철','버스','여객선','철도','항공','렌터카']):
            acct = '여비교통비'
        elif '커피전문점' in biz:
            acct = '복리후생비'
        elif '백화점' in biz:
            acct = '복리후생비'
        elif '호텔' in biz or '여관' in biz or '숙박' in biz:
            acct = '여비교통비'
        elif '골프' in biz or '화원' in biz or '화초' in biz:
            acct = '접대비'
        elif '우편' in biz:
            acct = '통신비'
        elif '공공편의' in biz or 'PG일반' in biz or '결제대행' in biz:
            acct = '지급수수료'
        elif '서적' in biz or '도서' in biz:
            acct = '도서인쇄비'
        elif '운반' in biz or '택배' in biz or '쿠팡' in store or '퀵서비스' in biz:
            acct = '소모품비'
        elif any(k in biz for k in ['인터넷상거래','체인스토어','기타상설','편의점',
                                      '슈퍼마켓','대형할인점','기타상품','잡화']):
            acct = '소모품비'
        elif '소모품' in biz:
            acct = '소모품비'
        elif '문구' in biz or '사무용' in biz:
            acct = '사무용품비'
        elif '치킨' in biz:
            acct = '복리후생비'
        elif '면세점' in biz:
            acct = '여비교통비'
        # 추가 분류 (분개장 정답 기반)
        elif '한국인삼공사' in biz or '인삼공사' in biz or '인삼' in biz:
            acct = '접대비'  # 선물/접대용
        elif '일반상품권' in biz or '상품권' in biz or '기프트' in biz:
            acct = '접대비'  # 선물용
        elif '약국' in biz:
            acct = '소모품비'  # 사무실 비치 약품
        elif '지방세' in biz:
            acct = '세금과공과금'
        elif '농산물' in biz or '청과' in biz or '농수산' in biz:
            acct = '소모품비'  # 회사 비치용
        else:
            unmapped.append({'날짜': f'{m}/{d}', '금액': amt, '가맹점': store,
                             '업종': biz, '카드': sheet_name})
            continue

        memo = biz if biz else store
        # 부가세 분리: 일반음식/카페 등은 매입금액에 부가세 포함, 카드 시트에 부가세 컬럼 없으면 단순 처리
        # 분개장 패턴 보면 1/11이 부가세
        부가세 = round(amt / 11)
        비용 = amt - 부가세
        if biz in ['일반음식점','일식','중식','서양식','패스트푸드','제과','아이스크림',
                    '기타식음료품','호프','기타음식점','커피전문점','백화점','일반주점']:
            rows += [row(m, d, '차변', '부가세대급금', '',  memo, 부가세, 0),
                     row(m, d, '차변', acct,           '',  memo, 비용,   0),
                     row(m, d, '대변', '미지급금',     cp,  memo, 0, amt)]
        else:
            rows += [row(m, d, '차변', acct,       '', memo, amt, 0),
                     row(m, d, '대변', '미지급금', cp, memo, 0, amt)]

    return rows, unmapped


