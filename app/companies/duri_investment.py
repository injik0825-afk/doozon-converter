    @staticmethod
    def format_partner(종목유형: str, 종목명: str, 시장구분: str = '') -> str:
        """
        종목명 + 유형 → 더존 거래처명 포맷

        분개장 형식:
        - 주식#코스피#케이뱅크
        - 채권#회사채#두산310-2
        - 채권#국민주택1종채권21-08 (3-segment, 시장구분 자리에 종목명)
        - 채권#국고채권02500-5203(22-2)
        - 펀드#어닝코스닥벤처일반사모투자신탁제1호
        """
        if 종목유형 == '주식':
            return f'주식#{시장구분 or "코스닥"}#{종목명}'
        if 종목유형 == '채권':
            # 국민주택1종/국고채는 #구분# 없이 종목명만
            if 종목명.startswith('국민주택') or 종목명.startswith('국고채'):
                return f'채권#{종목명}'
            return f'채권#{시장구분 or "회사채"}#{종목명}'
        if 종목유형 == '펀드':
            return f'펀드#{종목명}'
        return 종목명

    @staticmethod
    def normalize_security_name(name: str) -> str:
        """
        증권사 거래내역의 종목명 정규화 (공백/특수문자 제거)
        예: "이마트24신종자본증권 37" → "이마트24신종자본증권37"
            "주식회사 카나프테라퓨틱스" → "카나프테라퓨틱스"
        """
        if not name:
            return ''
        name = name.strip()
        # "주식회사 XX" 또는 "(주)XX" 제거
        name = re.sub(r'^주식회사\s+', '', name)
        name = re.sub(r'^\(주\)\s*', '', name)
        name = re.sub(r'\s*\(주\)$', '', name)
        # 종목명 끝의 공백+숫자(한자리~세자리 모두) 결합
        # "이마트24신종자본증권 37" → "이마트24신종자본증권37"
        name = re.sub(r'(\S)\s+(\d+)$', r'\1\2', name)
        return name

