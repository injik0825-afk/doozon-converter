def process_around_securities(xl, sheet_name, broker, cost_basis):
    """
    어라운드 증권사 거래내역서 통합 파서.
    
    증권사별 헤더 구조:
    - db증권: 1행 헤더 [거래일자/거래구분/거래수량/거래금액/세금/...종목명]
    - im증권: 메타 3행 + 2행 헤더 (행4+행5) + 2행 데이터 (선물거래)
    - kb증권: 2행 헤더 (행0+행1)
    - nh증권: 1행 헤더 [실거래일자/거래유형/상세내용/종목명/수량/단가/거래금액/정산금액]
    - 교보증권: 1행 헤더 [거래일자/번호/적요명/종목명/수량/단가/거래금액/정산금액/수수료]
    - 신영증권: 2행 헤더 (행0+행1)
    - 신한증권: 메타 3행 + 2행 헤더 (행3+행4)
    - 한양증권: 메타 2행 + 2행 헤더 (행2+행3) - 펀드
    - 한투증권: 2행 헤더 (행0+행1) + 2행 데이터
    
    공통 분개:
    - 매수: 차변 단매증 + 주식거래수수료 / 대변 예치금
    - 매도: 차변 예치금 + 수수료 + 거래세 / 대변 단매증 + 처분이익
    """
    rows, unmapped = [], []
    df_raw = pd.read_excel(xl, sheet_name=sheet_name, header=None).fillna('').astype(str)
    if df_raw.empty or df_raw.shape[0] < 2: return rows, unmapped

    cp_sec = f'{broker}증권({sheet_name})'

    # ── 증권사별 분기 ──
    if broker == 'db':
        # 1행 헤더, 행 1부터 데이터
        return _around_db(df_raw, cp_sec, cost_basis, sheet_name)
    elif broker == 'im':
        # 메타 4행, 행 4-5 헤더, 행 6부터 2행 1쌍 (선물거래)
        return _around_im(df_raw, cp_sec, cost_basis, sheet_name, broker)
    elif broker == 'kb':
        # 2행 헤더 (행0+행1), 행 2부터 2행 1쌍
        return _around_kb(df_raw, cp_sec, cost_basis, sheet_name)
    elif broker == 'nh':
        # 1행 헤더, 행 1부터 1행 1거래
        return _around_nh(df_raw, cp_sec, cost_basis, sheet_name)
    elif broker == '교보':
        # 1행 헤더, 행 1부터 1행 1거래
        return _around_kyobo(df_raw, cp_sec, cost_basis, sheet_name)
    elif broker == '신영':
        # 2행 헤더 (행0+행1), 행 2부터 2행 1쌍
        return _around_shinyoung(df_raw, cp_sec, cost_basis, sheet_name)
    elif broker == '신한':
        # 메타 3행, 행 3-4 헤더, 행 5부터 2행 1쌍
        return _around_shinhan(df_raw, cp_sec, cost_basis, sheet_name)
    elif broker == '한양':
        # 메타 2행, 행 2-3 헤더, 행 4부터 2행 1쌍 (펀드)
        return _around_hanyang(df_raw, cp_sec, cost_basis, sheet_name)
    elif broker == '키움':
        # 1행 헤더 + 1행 데이터 (예탁금이용료만)
        return _around_kiwoom(df_raw, cp_sec, cost_basis, sheet_name)
    elif broker == '한투':
        # 2행 헤더 (행0+행1), 행 2부터 2행 1쌍
        return _around_hantoo(df_raw, cp_sec, cost_basis, sheet_name)

    return rows, unmapped


def _around_record_buy(rows, cb, ck, m, d, sl, stock, cp_sec, qty, price, comm, cost):
    """매수 분개 공통 헬퍼"""
    memo = f'{sl}({qty}주)매수' if sl else f'{stock} 매수'
    rows.append(row(m, d, '차변', '단매증', sl, memo, cost, 0))
    if comm > 0:
        rows.append(row(m, d, '차변', '주식거래수수료', sl, '매수수수료', comm, 0))
    rows.append(row(m, d, '대변', '예치금', cp_sec, memo, 0, cost + comm))
    if ck in cb:
        old = cb[ck]
        nq = old['qty'] + qty
        cb[ck] = {'unit_price': (old['unit_price']*old['qty']+price*qty)/nq if nq else 0, 'qty': nq}
    else:
        cb[ck] = {'unit_price': price, 'qty': qty}


def _around_record_sell(rows, unmapped, cb, ck, m, d, sl, stock, cp_sec, qty, price, comm, tax, settle, broker):
    """매도 분개 공통 헬퍼"""
    if ck in cb and cb[ck]['qty'] >= qty:
        avg = cb[ck]['unit_price']
        acq_cost = avg * qty
        gain = settle + comm + tax - acq_cost
        cb[ck]['qty'] -= qty
        if cb[ck]['qty'] <= 0:
            del cb[ck]
    else:
        acq_cost = None
        gain = None

    memo = f'{sl}({qty}주)매도' if sl else f'{stock} 매도'
    rows.append(row(m, d, '차변', '예치금', cp_sec, memo, settle, 0))
    if comm > 0:
        rows.append(row(m, d, '차변', '주식거래수수료', sl, '매도수수료', comm, 0))
    if tax > 0:
        rows.append(row(m, d, '차변', '세금과공과금', sl, '증권거래세', tax, 0))

    if acq_cost is not None:
        rows.append(row(m, d, '대변', '단매증', sl, memo, 0, acq_cost))
        if gain > 0:
            rows.append(row(m, d, '대변', '처분이익', sl, memo + '처분이익', 0, gain))
        elif gain < 0:
            rows.append(row(m, d, '차변', '처분손실', sl, memo + '처분손실', abs(gain), 0))
    else:
        rows.append(row(m, d, '대변', '단매증', sl, f'{memo}[취득가확인]', 0, 0, 'RED'))
        rows.append(row(m, d, '대변', '처분이익', sl, f'{memo}[취득가확인]', 0, 0, 'RED'))
        unmapped.append({'날짜': f'{m}/{d}', '종목': stock, '수량': qty, '단가': price,
                         '비고': f'{broker} 취득가 확인 필요'})


def _around_db(df, cp_sec, cb, sheet_name):
    """db증권 1행 헤더"""
    rows, unmapped = [], []
    for i in range(1, len(df)):
        r = df.iloc[i]
        date_val = clean(r.iloc[0])
        if not date_val: continue
        m, d = parse_date(str(date_val))
        if not m: continue

        ttype = clean(r.iloc[1])
        qty = abs(to_int(r.iloc[2]))
        amount = to_int(r.iloc[3])
        tax = to_int(r.iloc[5])
        stock = clean(r.iloc[12]) if df.shape[1] > 12 else ''
        sl = get_stock(stock) if stock else ''
        ck = (stock, sheet_name)

        if '매도' in ttype and qty > 0 and amount > 0:
            settle = amount - tax  # db는 수수료 별도 표기 안 됨
            comm = 0
            price = amount // qty if qty else 0
            _around_record_sell(rows, unmapped, cb, ck, m, d, sl, stock, cp_sec, qty, price, comm, tax, settle, 'db')
    return rows, unmapped


