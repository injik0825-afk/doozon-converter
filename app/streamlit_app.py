def clean(v):
    if v is None: return ''
    try:
        if pd.isna(v): return ''
    except: pass
    return str(v).strip()

def to_int(v):
    try: return int(float(str(v).replace(',', '').replace(' ', '')))
    except: return 0

def parse_date(v):
    s = clean(v)
    if not s: return None, None
    try:
        d = pd.to_datetime(s[:10])
        return d.month, d.day
    except: pass
    try:
        d = pd.to_datetime(v)
        return d.month, d.day
    except: return None, None

