# ── 엔라이튼 법인카드 처리 ────────────────────────────────────────────────────
def process_enra_corp_card(df):
    """
    엔라이튼 법인카드승인내역 파서.
    
    데이터 구조:
    - 첫 부분: 요약정보 (승인건수, 매입건수 등)
    - 헤더 영역: [NO, 이용일, 이용시간, 카드번호, 승인번호, 승인금액, 승인취소금액,
                  가맹점명, 업종명, 가맹점번호, 사업자번호, 이용구분, 할부, 매입,
                  매입금액, 매출취소, 매입일, 상태, 부가세, 하위몰]
    - 카드번호 포맷: 5531-****-****-895 → 끝 4자리만 추출 (마지막 3자 + 1자 = 사실은 8951 같은)
                     주의: 분개장은 '하나법인/3972' 형식으로 4자리
    
    분개:
    - 차변 부가세대급금 / 비용계정 (90%)
    - 대변 미지급금 (전액, 거래처 = 하나법인/XXXX)
    
    업종/가맹점명 → 비용계정:
    - 일반음식점/일식/중식/서양식/패스트푸드/제과제빵/아이스크림/편의점/기타식음료품 → 복리후생비(접대비 부분만 별도)
    - 호텔(특급) → 접대비 또는 여비교통비
    - 택시/주유소/고속도로통행/주차 → 여비교통비
    - 백화점 → 복리후생비 또는 접대비
    - PG일반 → 지급수수료
    - 항공권 → 여비교통비
    """
    rows, unmapped = [], []
    if df.empty: return rows, unmapped

    # 헤더 찾기 (NO/이용일/카드번호 행)
    hdr = None
    for i in range(min(15, len(df))):
        vals = [clean(v) for v in df.iloc[i].values]
        if 'NO' in vals and '이용일' in vals and '카드번호' in vals:
            hdr = i; break
    if hdr is None: return rows, unmapped

    df.columns = df.iloc[hdr]
    df = df.iloc[hdr+1:].reset_index(drop=True)

    for _, r in df.iterrows():
        date_val = clean(r.get('이용일', ''))
        if not date_val: continue
        m, d = parse_date(str(date_val))
        if not m: continue

        # 상태 = 정상만 처리 (취소 제외)
        status = clean(r.get('상태', ''))
        if status and status != '정상': continue

        amt = to_int(r.get('승인금액', 0))
        if amt <= 0: continue

        # 매입금액 + 부가세 (분개에서 부가세 분리)
        매입 = to_int(r.get('매입금액', 0))
        부가세 = to_int(r.get('부가세', 0))
        # 일반음식점 등은 부가세 = 매입의 약 1/11
        # 부가세대급금 = 부가세, 비용 = 매입 - 부가세

        store = clean(r.get('가맹점명', ''))
        biz   = clean(r.get('업종명', ''))
        card_no = clean(r.get('카드번호', ''))

        # 카드번호 끝 4자리 추출 (5531-****-****-3972 → 3972)
        m_card = re.search(r'-(\d{4})$', card_no)
        card_short = m_card.group(1) if m_card else 'XXXX'
        cp = f'하나법인/{card_short}'

        # 비용계정 분류 (가맹점명 + 업종명 종합)
        all_text = store + ' ' + biz

        # 일반음식점/식당 → 복리후생비
        if any(k in biz for k in ['일반음식점','일식','중식','서양식','패스트푸드',
                                    '제과','아이스크림','기타식음료품','호프','기타음식점']):
            acct = '복리후생비'
